# Dobro, triplo e raíz quadrada

num = int(input('\nDigite um número: '))

print('\n======================')

dobro = num * 2

triplo = num * 3

raiz = num ** (1/2)

print('\nO dobro de {} é {}'.format(num, dobro))

print('\nO triplo é {}'.format(triplo))

print('\nE a raíz é {:.3f}'.format(raiz))

print('\n======================')
