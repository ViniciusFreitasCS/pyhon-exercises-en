# Dobro, triplo e raíz quadrada

num = int(input('\nDigite um número: '))

print('\n======================')

dobro = num * 2

triplo = num * 3

raiz = num ** (1/2)

print(f'\nO dobro de {num} é {dobro}')

print(f'\nO triplo é {triplo}')

print(f'\nE a raíz é {raiz:.2f}')

print('\n======================')
